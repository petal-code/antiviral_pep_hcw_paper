# with grey Warsame UFC points and an empirical uncertainty ribbon.
#
# This is the version intended to match the best conflict plot:
#   * preserve the existing jagged/conflict-interrupted q_value shape;
#   * lock parameter endpoints to early/late literature extrema;
#   * force terminal-zero unsafe-funeral anchors to plateau at zero;
#   * show grey Warsame rolling SDB points only in the community unsafe-funeral panel;
#   * add an uncertainty ribbon derived from uncertainty in the Warsame SDB backbone;
#   * keep orange literature anchors labelled.
#
# Important methodological note:
#   A smooth Stan partial-pooling fit of Q_j(t) will not preserve the jagged
#   empirical conflict shape. Here the empirical Q shape is treated as fixed,
#   which is the intended DRC-conflict specification. The ribbon is therefore
#   uncertainty around that empirical Q backbone rathe                                                r than a re-estimated
#   smooth logistic Q curve.
