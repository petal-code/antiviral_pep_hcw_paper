for (p in matrix_cols) {
  e <- endpoint_table %>% filter(parameter == p) %>% slice(1)

  if (nrow(e) == 0) {
    matrix_out[[p]] <- NA_real_
    next
  }

  if (p == "prob_unsafe_funeral_comm") {
    # For DRC conflict, community unsafe funerals are the absolute Warsame
    # unsafe-funeral proxy from the first grey Warsame point. This exactly mirrors
    # the grey SDB points and deliberately has no CI ribbon.
    mean_vec <- ufc_direct_curve
    q5_vec <- mean_vec
    q95_vec <- mean_vec
  } else if (e$direction == "flat") {
    mean_vec <- rep(e$start_value, nrow(matrix_out))
    q5_vec <- mean_vec
    q95_vec <- mean_vec
  } else {
    mean_vec <- e$start_value + (e$end_value - e$start_value) * q
    low_candidate <- e$start_value + (e$end_value - e$start_value) * q_low
    high_candidate <- e$start_value + (e$end_value - e$start_value) * q_high
    q5_vec <- pmin(low_candidate, high_candidate)
    q95_vec <- pmax(low_candidate, high_candidate)

    if (e$end_endpoint_source == "terminal_zero_anchor_anywhere" && is.finite(e$end_anchor_day)) {
      plateau_idx <- matrix_out$relative_day >= e$end_anchor_day
      mean_vec[plateau_idx] <- e$end_value
      q5_vec[plateau_idx] <- e$end_value
      q95_vec[plateau_idx] <- e$end_value
    }
  }

  matrix_out[[p]] <- mean_vec

  matrix_uncertainty_long[[p]] <- tibble(
    parameter = p,
    relative_day = matrix_out$relative_day,
    mean = mean_vec,
    q5 = q5_vec,
    q95 = q95_vec
  )
}

matrix_uncertainty_long <- bind_rows(matrix_uncertainty_long)

readr::write_csv(matrix_out, paste0(out_prefix, "_matrix.csv"))
readr::write_csv(matrix_uncertainty_long, paste0(out_prefix, "_matrix_with_uncertainty_long.csv"))
